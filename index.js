function suma(a, b) {
	return a + b;
}
module.exports.suma = suma;

function resta(a, b) {
	return a - b;
}
module.exports.resta = resta;

function multiplicacion(a, b) {
	return a * b;
}
module.exports.multiplicacion = multiplicacion;

function division(a, b) {
	return a / b;
}
module.exports.division = division;
